"""المرحلة 5: تطبيع أرقام الهاتف — مصدر مركزي واحد.

يُستخدَم في الحجز العام (إنشاء/بحث/إدارة/إلغاء/تقييم) وفي الإشعارات، كي لا يفشل
البحث عن الحجز أو إدارته لمجرّد اختلاف صيغة كتابة الرقم:

    +963 944 123 456   ┐
    00963-944-123456   ├──►  963944123456
    0944123456         ┘
    ٠٩٤٤١٢٣٤٥٦          ►     963944123456   (أرقام عربية → إنجليزية)

القواعد بسيطة وآمنة عمدًا (لا حلّ دوليّ مثاليّ): أرقام إنجليزية فقط، بلا رموز/
مسافات، معالجة بادئتَي 00/0 (كود افتراضي). لا نفترض كود الدولة إن لم تظهر بادئة
محلية (0/00) — تفاديًا لربط أرقام مختلفة ببعضها بالخطأ.
"""

# أرقام عربية-هندية (U+0660–U+0669) وفارسية/ممتدة (U+06F0–U+06F9) → إنجليزية.
_AR_DIGITS = str.maketrans('٠١٢٣٤٥٦٧٨٩۰۱۲۳۴۵۶۷۸۹', '01234567890123456789')


def normalize_phone(phone, default_cc='963'):
    """يُرجع الرقم بأرقام إنجليزية فقط بصيغة دولية مبسّطة (بلا + أو مسافات/شرطات/
    أقواس). خطوات: تحويل الأرقام العربية/الفارسية → أرقام فقط → «00xxxx»→«xxxx» و
    «0xxxx»→«<cc>xxxx». يُرجع '' إن لم توجد أرقام. لا يفترض دولة بلا بادئة محلية."""
    s = (phone or '').translate(_AR_DIGITS)
    digits = ''.join(ch for ch in s if ch in '0123456789')
    if not digits:
        return ''
    if digits.startswith('00'):
        digits = digits[2:]
    elif digits.startswith('0'):
        digits = default_cc + digits[1:]
    return digits
